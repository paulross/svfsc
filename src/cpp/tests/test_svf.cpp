    size_t test_write_all(t_test_results &results) {
        size_t count = 0;
        for (const auto& test_case: write_test_cases) {
            results.push_back(test_case.run());
            count += 1;
        }
        return count;
    }


    size_t test_all(t_test_results &results) {
        size_t count = 0;
        count += test_write_all(results);
        return count;
    }

